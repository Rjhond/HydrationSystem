        private void populateComboBox() {
            DefaultComboBoxModel<String> hoursModel = new DefaultComboBoxModel<>();
            hoursModel.addElement("Select Hours");
            for (int i = 1; i <= 12; i++) {
                hoursModel.addElement(String.format("%02d", i));
            }
            cbohours.setModel(hoursModel);

            DefaultComboBoxModel<String> minutesModel = new DefaultComboBoxModel<>();
            minutesModel.addElement("Select Minute");
            for (int i = 0; i < 60; i++) {
                minutesModel.addElement(String.format("%02d", i));
            }
            cbominute.setModel(minutesModel);
        } 
        

