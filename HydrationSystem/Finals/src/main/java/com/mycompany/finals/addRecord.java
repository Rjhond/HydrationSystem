    private void btnokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnokActionPerformed
        // TODO add your handling code here:

        hour = (String)cbohours.getSelectedItem();
        minute = (String)cbominute.getSelectedItem();
        amPm = rbAm.isSelected() ? "AM" : "PM";
        ml = txtml.getText();
        time = hour + ":" + minute + " " + amPm;
              
        if(ml.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter the amount of water");
            return;
        }


            try (PreparedStatement pstmt = conn.prepareStatement("INSERT INTO water_intake_log (usersid, intake_time, intake_ml, intake_date) VALUES (?, STR_TO_DATE(?, '%h:%i %p'), ?, CURDATE())")) {
                pstmt.setInt(1, userId);
                pstmt.setString(2, time);
                pstmt.setInt(3, Integer.parseInt(ml));

                int row = pstmt.executeUpdate();
                if (row > 0) {
                    JOptionPane.showMessageDialog(this, "Record successfully added");
                    Dashboard db = new Dashboard();
                    db.setUserId(userId);
                    db.setVisible(true);
                    setVisible(false);
                    db.setLocationRelativeTo(null);
                    db.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Error: Could not add the record, please try again");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }


    }//GEN-LAST:event_btnokActionPerformed

